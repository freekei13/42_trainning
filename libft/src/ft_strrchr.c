#include "../includes/libft.h"

char	*ft_strrchr(const char *str, int c)
{
	const unsigned char	*s;
	unsigned char		ch;
	const unsigned char	*last;

	s = (const unsigned char *)str;
	ch = (unsigned char)c;
	last = NULL;
	while (*s)
	{
		if (*s == ch)
			last = s;
		s++;
	}
	if (ch == '\0')
		return ((char *)s);
	return ((char *)last);
}
